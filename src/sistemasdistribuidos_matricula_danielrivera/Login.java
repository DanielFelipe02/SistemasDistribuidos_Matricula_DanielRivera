    private void btnInciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInciarActionPerformed
        // Definir las variables a utilizar
            String usuario;
            String password;

        try{
            //Valido los campos vacios
            if (txtUsuario.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Digite el usuario");
                txtUsuario.requestFocus();
               return;
            }
            if (txtPassword.getPassword().length==0) {
              JOptionPane.showMessageDialog(null, "Digite la contrase침a");
              txtPassword.requestFocus();
              return;
            }
            //Asigno los elementos graficos a las variables
            usuario=txtUsuario.getText();
            char cadena[]=txtPassword.getPassword();
            password=new String(cadena);
         
             //Procesos
            if (usuario.equals("admin")&& password.equals("123")) {
                //iniciamos Sesi칩n-instanciamos
                Enroll obj= new Enroll();
                obj.setVisible(true);
                this.setVisible(false);
            } 
            else {
                //imprimo nombre de usuario o password incorrecto
                JOptionPane.showMessageDialog(null, "Usuario o contrase침a incorrecta");
                txtUsuario.setText("");
                txtPassword.setText("");
                txtUsuario.requestFocus();
    }//GEN-LAST:event_btnInciarActionPerformed
        }
        catch (Exception e) {
        JOptionPane.showMessageDialog(null,"Error:"+ e);
        }
    }
